import turtle
turtle.Screen().bgcolor("white")
turtle.Screen().setup(500,500)
b=turtle.Turtle()
b.forward(100)
b.left(120)
b.forward(100)
b.left(120)
b.forward(100)

b.penup()
b.right(150)
b.forward(50)

b.pendown()
b.right(90)
b.forward(100)
b.right(120)
b.forward(100)
b.right(120)
b.forward(100)

turtle.done()